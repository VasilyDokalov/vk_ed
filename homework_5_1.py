def format_numbers():
    # Ввод трех чисел
    a = int(input("Введите целое число: "))
    b = float(input("Введите число с плавающей точкой: "))
    c = int(input("Введите целое неотрицательное число: "))

    # Форматирование первого числа (целого)
    formatted_a = f"{a:+012d}"

    # Форматирование второго числа (с плавающей точкой)
    formatted_b = f"{b:0>10.2f}"

    # Форматирование третьего числа (целого неотрицательного)
    formatted_c = f"{c:012b}".replace('0', '_')

    # Вывод отформатированных данных
    print(f"{formatted_a} {formatted_b} {formatted_c}")

# Вызов функции для примера использования
format_numbers()